# This code prompts the user to input a two-digit number and stores it in the variable 'num'
num = input('Input a Two Digit Number: ')

# The code then adds the first and second digits of the input number and stores the result in the variable 'sum'
sum = int(num[0]) + int(num[1])

# Finally, the code prints the sum of the two digits to the console
print(sum)

